export const searchMachines = (query: string): Machine[] => {
  const q = query.toLowerCase();
  return MACHINES.filter(
    (m) =>
      m.name.toLowerCase().includes(q) ||
      m.maker.toLowerCase().includes(q) ||
      m.category.toLowerCase().includes(q),
  );
};
